def _merge_keywords():
    """合并手动和自动关键词，手动优先级更高"""
    merged = {}
    
    # 先加入自动关键词
    for industry, keywords in INDUSTRY_KEYWORDS_AUTO.items():
        merged[industry] = list(keywords)
    
    # 手动关键词覆盖/补充
    for industry, keywords in INDUSTRY_KEYWORDS_MANUAL.items():
        if industry in merged:
            # 合并（手动优先）
            merged[industry] = keywords + [k for k in merged[industry] if k not in keywords]
        else:
            merged[industry] = keywords
    
    return merged


# 最终的关键词映射（合并后）
INDUSTRY_KEYWORDS = _merge_keywords()


def get_industry_keywords(industry: str) -> list:
    """获取指定行业的关键词列表"""
    return INDUSTRY_KEYWORDS.get(industry, [industry])


def get_all_industries() -> list:
    """获取所有行业名称（合并后）"""
    return list(INDUSTRY_KEYWORDS.keys())


def match_industry(text: str) -> list:
    """
    从文本中匹配行业
    
    Args:
        text: 新闻标题或内容
    
    Returns:
        匹配到的行业列表
    """
    text_lower = text.lower()
    matched = []
    
    for industry, keywords in INDUSTRY_KEYWORDS.items():
        for kw in keywords:
            if kw.lower() in text_lower:
                matched.append(industry)
                break  # 每个行业只匹配一次
    
    return matched


