def concept_to_industries(concept: str) -> list:
    """
    将概念板块映射到申万一级行业
    
    Args:
        concept: 概念板块名称（如"人工智能"）
    
    Returns:
        申万一级行业列表（如["计算机", "电子", "通信"]）
    """
    return CONCEPT_TO_INDUSTRY_MAP.get(concept, ['综合'])


def concepts_to_industries(concepts: list) -> list:
    """
    批量将概念板块映射到申万一级行业
    
    Args:
        concepts: 概念板块列表
    
    Returns:
        去重后的申万一级行业列表
    """
    industries = set()
    for concept in concepts:
        for ind in concept_to_industries(concept):
            industries.add(ind)
    return list(industries)


def industry_to_concepts(industry: str) -> list:
    """
    反向查询：申万一级行业属于哪些概念板块
    
    Args:
        industry: 申万一级行业名称
    
    Returns:
        概念板块列表
    """
    concepts = []
    for concept, industries in CONCEPT_TO_INDUSTRY_MAP.items():
        if industry in industries:
            concepts.append(concept)
    return concepts


def get_all_concepts() -> list:
    """获取所有概念板块名称"""
    return list(CONCEPT_TO_INDUSTRY_MAP.keys())


def get_all_industries() -> list:
    """获取所有涉及的申万一级行业"""
    industries = set()
    for inds in CONCEPT_TO_INDUSTRY_MAP.values():
        for ind in inds:
            industries.add(ind)
    return sorted(list(industries))


